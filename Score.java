public class Score{
	
	private int score = 0;
	/**
	 * An accessor to access the score
	 *
	 * @return	returns the current score	
	 */
	public int getScore(){
		return score;
	}

	/**
	 * A mutator to set new score
	 *
	 */
	public void setScore(int newScore) {
		score = newScore;
	}
	
	public Score(){}
	
}
